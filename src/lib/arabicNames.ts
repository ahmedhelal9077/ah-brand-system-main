export const isNameWord = (word: string) => {
  if (!word) return false;
  return arabicNames.has(word);
};

export function extractNameAndAddress(text: string): { name: string, address: string } {
  const addressIndicators = ["شارع", "ش", "ميدان", "منطقة", "مساكن", "مدينة", "كفر", "عزبة", "مسجد", "مستشفى", "مدرسة", "بجوار", "امام", "خلف", "تقاطع", "برج", "عمارة", "تقسيم", "طريق", "كوبري", "نفق", "موقف", "محطة", "دوران"];
  const ignorePrefixes = ["شحن", "ارسال", "إرسال", "توصيل", "مطلوب", "اوردر", "أوردر", "طلب", "لشحن", "لاستلام", "تسليم", "الاسم", "باسم", "اسم", "المستلم", "العميل", "لعميل", "الاستلام", "التوصيل", "الي", "إلى", "علي", "على", "عنوان", "العنوان", "محافظة", "المحافظة"];
  
  let cleanText = text.trim();
  let changed = true;
  while(changed) {
    changed = false;
    for (const prefix of ignorePrefixes) {
      if (cleanText.startsWith(prefix + " ") || cleanText.startsWith(prefix + ":") || cleanText.startsWith(prefix + "-") || cleanText === prefix) {
        cleanText = cleanText.substring(prefix.length).replace(/^[\s:\-]+/, "");
        changed = true;
      }
    }
  }

  const words = cleanText.split(/\s+/).filter(w => w.length > 0);
  if (words.length === 0) {
    return { name: "", address: cleanText };
  }

  const isFirstWordCityOrAddress = addressIndicators.includes(words[0]) || Object.keys(egyptCitiesMap).some(c => cleanText.startsWith(c));

  if (words.length <= 2 && !isFirstWordCityOrAddress) {
    return { name: cleanText, address: "" };
  }

  let sequences: { startIndex: number, endIndex: number, words: string[], score: number }[] = [];
  
  let i = 0;
  while (i < words.length) {
    if (isNameWord(words[i])) {
      let start = i;
      let nameWords = [words[i]];
      let j = i + 1;
      
      while (j < words.length && isNameWord(words[j])) {
        nameWords.push(words[j]);
        j++;
      }
      
      let score = nameWords.length;
      
      if (start === 0) score += 2;
      if (j === words.length) score += 2;
      
      if (start > 0 && addressIndicators.includes(words[start - 1])) {
        score -= 10;
      }

      sequences.push({ startIndex: start, endIndex: j - 1, words: nameWords, score });
      
      i = j;
    } else {
      i++;
    }
  }

  if (sequences.length === 0) {
    if (isFirstWordCityOrAddress) {
      return { name: "", address: cleanText };
    }
    return { name: words.slice(0, 2).join(" "), address: words.slice(2).join(" ") };
  }

  sequences.sort((a, b) => b.score - a.score);
  const best = sequences[0];

  if (best.score <= 0) {
    if (isFirstWordCityOrAddress || addressIndicators.includes(words[0])) {
      return { name: "", address: cleanText };
    } else {
      return { name: words.slice(0, 2).join(" "), address: words.slice(2).join(" ") };
    }
  }

  const name = best.words.join(" ");
  const addressWords = [...words.slice(0, best.startIndex), ...words.slice(best.endIndex + 1)];
  const address = addressWords.join(" ");

  return { name, address };
}
